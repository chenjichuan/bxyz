        　　(一)申报对象

        　　申请支持的企业为成立时间在5年(含)以内，从业人员100人(含)以下，上一年度研发费用20万元(含)以上，营业收入1000万元-2000万元(含)以下的中关村高新技术企业(或在示范区范围内注册的国家高新技术企业)。

        　　(二)支持标准

        　　(1)企业上一年研发费用总额在20-100万元的，按不超过支出额的5%给予补助。

        　　(2)企业上一年研发费用支出额高于(含)100万元、不足500万元的，按照每家不超过10万元给予补助。

        　　(3)企业上一年研发费用支出额高于(含)500万元的，按照每家不超过20万元给予补助。

        　　二、申报途径

        　　此次申报采取线上系统申报，在中关村示范区网站(www.zgcgw.beijing.gov.cn)统一填报。

        　　三、申报时间

        　　拟于2020年4月底起启动第二批中关村科技型小微企业研发费用支持资金受理工作。

        　　四、审核流程

        　　2020年中关村科技型小微企业研发经费支持政策申报审核包括四个环节：

        　　1、委托机构审核。由中关村创业生态发展促进会负责组织实施，审查申报企业基本资质(成立年限、是否中关村高新技术企业或国家高新技术企业、申报上年度研发投入是否达到最低支持标准等)和企业征信情况。

        　　2、第三方审核。由北京银行中关村分行等10家金融机构组织实施，根据研发经费归集标准核定企业上年度研发投入金额，并形成初审意见。

        　　3、分园审核。由各分园管委会组织实施，对初审结果和第三方审核结果进行复核，形成审核意见提交中关村管委会。

        　　4、终审。由中关村管委会组织实施，对分园审核结果进行核查，根据企业上一年度研发经费核准情况确定支持标准，形成支持企业名单并组织公示，将公示无异议的企业名单和支持标准提交各区。</pre>
